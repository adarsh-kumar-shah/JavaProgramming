import java.util.Scanner;

public class SwapNumbers {
    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        System.out.println("Value of a is " + a + " Value of b is " + b);
        int temp = a;
        a=b;
        b=temp;
        System.out.println("Value of a is " + a + " Value of b is " + b);


        
    }
}
